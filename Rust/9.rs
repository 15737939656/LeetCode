impl Solution {
    pub fn is_palindrome(x: i32) -> bool {
        if x < 0 {
            return false;
        }
        let mut temp = x;
        let mut s = 0;
        while temp != 0 {
            s = s * 10 + temp % 10;
            temp /= 10;
        }
        if s == x {
            return true;
        } else {
            return false;
        }
    }
}
